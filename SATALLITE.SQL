CREATE DATABASE SATALLITE;
USE SATALLITE;
CREATE TABLE SATALLITE(
SATALLITEID INT PRIMARY KEY,
SATALLITENAME VARCHAR(255) NOT NULL,
LAUNCHDATE DATE,
LAUNCHVEHICLE VARCHAR(100),
ORBITTYPE VARCHAR(50), 
MASSKG DECIMAL(15,2),
MANUFACTURE  VARCHAR(255),
COUNTRY VARCHAR(100),
OPERATOR VARCHAR(100),
PURPOSE TEXT,
APOGEEKM DECIMAL(15,2),
PERIGEEM DECIMAL(15,2),
ECCENTRICITY DECIMAL(5,4),
INCLINATIONDEGREES DECIMAL(8,4),
PERIODMINUTES DECIMAL(10,2),
TRANSPONDERFREQUENCY VARCHAR(50),
POWERSOURCE VARCHAR(50),
OPERATIONALSTATUS VARCHAR(50),DATEOUTOFSERVICE DATE,
NOTES TEXT);




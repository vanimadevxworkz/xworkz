package com.xworkz.test.driver;

public class Operators {
	public static void main(String args[]) {
		int a=10;
		int b=15;
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
		System.out.println(a%b);
		System.out.println(a>=b);
		System.out.println(a<=b);
		System.out.println(a==b);
		System.out.println(a>b);
		System.out.println(a<b);
		System.out.println((a<b)? true:false);
		System.out.println(a++);
		System.out.println(a--);
		System.out.println(a+=b);
		System.out.println(a-=b);
		System.out.println(a*=b);
		System.out.println(a/=b);
	System.out.println(a%=b);
	System.out.println(a=b);
	System.out.println(a!=b);
	}
}
